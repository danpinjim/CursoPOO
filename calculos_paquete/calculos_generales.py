def sumar(op1,op2):
    print('El resultado de la operacion es: ',op1+op2)

def restar(op1,op2):
    print('El resultado de la operacion es: ',op1-op2)

def multiplicar(op1,op2):
    print('El resultado de la operacion es: ',op1*op2)

def dividir(dividendo,divisor):
    print('El resultado de la operacion es: ',dividendo/divisor)

def potencia(base,exponente):
    print('El resultado de la operacion es: ',base**exponente)

def redondear(numero):
    print('El resultado de la operacion es: ',round(numero))

